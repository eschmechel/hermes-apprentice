// Package augment expands small datasets (< 200 pairs) by generating
// lexically-varied paraphrases of each input via an LLM.  The original
// output text is preserved for each paraphrase.  Satisfies dataset-builder-06.
package augment

import (
	"bytes"
	"context"
	"encoding/json"
	"fmt"
	"io"
	"math"
	"net/http"
	"strings"
	"time"

	"github.com/eschmechel/hermes-apprentice/dataset-builder/internal/fetcher"
)

const (
	defaultMinTarget       = 200
	defaultMaxParaphrases  = 10
	defaultModel           = "google/gemini-2.5-flash-preview"
	defaultBaseURL         = "https://openrouter.ai/api/v1"
	paraphraseSystemPrompt = `You are a paraphrase generator.  Given an instruction or prompt,
rewrite it in different words while preserving its EXACT meaning, task, tone,
and expected output format.  Do NOT add or remove requirements — only rephrase.
Return ONLY the paraphrased text with no commentary, quotes, or markdown.`
)

// Config holds parameters for the augmentation service.
type Config struct {
	// Model is the provider-specific model ID.  Defaults to
	// "google/gemini-2.5-flash-preview".
	Model string

	// APIKey is the OpenRouter API key (passed as Bearer token).
	APIKey string

	// BaseURL overrides the default OpenRouter endpoint.
	BaseURL string

	// MinTarget is the dataset size to reach via augmentation.  If the
	// input already contains at least MinTarget records, no augmentation
	// occurs.  Defaults to 200.
	MinTarget int

	// MaxParaphrases caps how many paraphrases are generated per
	// individual input record.  Defaults to 10.
	MaxParaphrases int

	// HTTPClient is optional; a 30 s timeout is used when nil.
	HTTPClient *http.Client
}

// Augmenter calls an LLM to paraphrase inputs and expand a small dataset.
type Augmenter struct {
	cfg    Config
	client *http.Client
}

// New returns a ready-to-use Augmenter.  APIKey must be non-empty.
func New(cfg Config) (*Augmenter, error) {
	if cfg.APIKey == "" {
		return nil, fmt.Errorf("augment: APIKey is required")
	}
	if cfg.Model == "" {
		cfg.Model = defaultModel
	}
	if cfg.BaseURL == "" {
		cfg.BaseURL = defaultBaseURL
	}
	if cfg.MinTarget <= 0 {
		cfg.MinTarget = defaultMinTarget
	}
	if cfg.MaxParaphrases <= 0 {
		cfg.MaxParaphrases = defaultMaxParaphrases
	}
	client := cfg.HTTPClient
	if client == nil {
		client = &http.Client{Timeout: 30 * time.Second}
	}
	return &Augmenter{cfg: cfg, client: client}, nil
}

// MinTarget exposes the configured threshold so callers can check whether
// augmentation is needed without examining Config internals.
func (a *Augmenter) MinTarget() int { return a.cfg.MinTarget }

// Augment expands the input records until the total reaches MinTarget.  If
// the input already has >= MinTarget records it is returned unchanged.
//
// Paraphrases are generated by calling the LLM per input record.  Each
// paraphrase becomes a new record that shares the original OutputText,
// metadata fields, and a copied InputHash (from the parent record — the
// downstream pipeline re-hashes after augmentation).
func (a *Augmenter) Augment(ctx context.Context, records []fetcher.Record) ([]fetcher.Record, error) {
	if len(records) == 0 {
		return nil, fmt.Errorf("augment: at least one record is required")
	}
	if len(records) >= a.cfg.MinTarget {
		return records, nil
	}

	totalNeeded := a.cfg.MinTarget - len(records)
	perRecord := int(math.Ceil(float64(totalNeeded) / float64(len(records))))
	if perRecord > a.cfg.MaxParaphrases {
		perRecord = a.cfg.MaxParaphrases
	}

	out := make([]fetcher.Record, 0, a.cfg.MinTarget)
	out = append(out, records...)

	for i := range records {
		paraphrases, err := a.paraphrase(ctx, records[i].InputText, perRecord)
		if err != nil {
			// Log and continue — a single record's failure shouldn't
			// abort the entire augmentation batch.
			continue
		}
		for _, p := range paraphrases {
			p = strings.TrimSpace(p)
			if p == "" || p == records[i].InputText {
				continue
			}
			rec := records[i]
			rec.InputText = p
			out = append(out, rec)
		}
	}

	return out, nil
}

// paraphrase requests N distinct paraphrases of input from the LLM.
func (a *Augmenter) paraphrase(ctx context.Context, input string, count int) ([]string, error) {
	userPrompt := fmt.Sprintf(
		"Rewrite the following instruction %d different way(s) in different words while preserving its exact meaning and purpose.  Number each paraphrase like 1) ... 2) ... etc.\n\nOriginal: %s",
		count, input,
	)

	body := openAIRequest{
		Model: a.cfg.Model,
		Messages: []openAIMessage{
			{Role: "system", Content: paraphraseSystemPrompt},
			{Role: "user", Content: userPrompt},
		},
		Temperature: 0.7,
		MaxTokens:   1024,
	}
	reqBytes, err := json.Marshal(body)
	if err != nil {
		return nil, fmt.Errorf("marshal request: %w", err)
	}

	url := strings.TrimRight(a.cfg.BaseURL, "/") + "/chat/completions"
	req, err := http.NewRequestWithContext(ctx, http.MethodPost, url, bytes.NewReader(reqBytes))
	if err != nil {
		return nil, fmt.Errorf("new request: %w", err)
	}
	req.Header.Set("Authorization", "Bearer "+a.cfg.APIKey)
	req.Header.Set("Content-Type", "application/json")

	resp, err := a.client.Do(req)
	if err != nil {
		return nil, fmt.Errorf("openrouter call: %w", err)
	}
	defer resp.Body.Close()

	respBytes, err := io.ReadAll(io.LimitReader(resp.Body, 1<<20))
	if err != nil {
		return nil, fmt.Errorf("read response: %w", err)
	}
	if resp.StatusCode != http.StatusOK {
		return nil, fmt.Errorf("openrouter returned %d: %s", resp.StatusCode, string(respBytes))
	}

	var cr openAIResponse
	if err := json.Unmarshal(respBytes, &cr); err != nil {
		return nil, fmt.Errorf("unmarshal response: %w", err)
	}
	if len(cr.Choices) == 0 {
		return nil, fmt.Errorf("openrouter returned no choices")
	}

	return parseNumbered(cr.Choices[0].Message.Content, count), nil
}

// parseNumbered extracts numbered paraphrases from a response like
// "1) first paraphrase\n2) second paraphrase\n..."
func parseNumbered(text string, expected int) []string {
	var out []string
	lines := strings.Split(text, "\n")
	for _, line := range lines {
		line = strings.TrimSpace(line)
		if line == "" {
			continue
		}
		// Strip leading "1) " or "1." or "1 - " prefix.
		idx := 0
		for idx < len(line) && line[idx] >= '0' && line[idx] <= '9' {
			idx++
		}
		if idx == 0 || idx >= len(line)-1 {
			continue
		}
		rest := line[idx:]
		if strings.HasPrefix(rest, ") ") || strings.HasPrefix(rest, ". ") || strings.HasPrefix(rest, " - ") {
			rest = rest[2:]
		} else if strings.HasPrefix(rest, ")") || strings.HasPrefix(rest, ".") || strings.HasPrefix(rest, "-") {
			rest = rest[1:]
		} else {
			continue
		}
		rest = strings.TrimSpace(rest)
		if rest != "" {
			out = append(out, rest)
		}
	}
	if len(out) > expected {
		out = out[:expected]
	}
	return out
}

// ---------- JSON types (OpenAI-compatible) ----------

type openAIRequest struct {
	Model       string         `json:"model"`
	Messages    []openAIMessage `json:"messages"`
	Temperature float64        `json:"temperature"`
	MaxTokens   int            `json:"max_tokens"`
}

type openAIMessage struct {
	Role    string `json:"role"`
	Content string `json:"content"`
}

type openAIResponse struct {
	Choices []struct {
		Message openAIMessage `json:"message"`
	} `json:"choices"`
}
